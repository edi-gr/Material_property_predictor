export type MaterialPrediction = {
  energyAboveHull: number;
  bandGap: number;
  isMetal: boolean;
  totalMagnetization: number;
};

export function predictProperties(
  formula: string,
  crystalSystem: string,
  spaceGroup: string
): MaterialPrediction {
  // Find exact match first
  const exactMatch = materialData.find(
    (d) =>
      d.formula === formula &&
      d.crystalSystem === crystalSystem &&
      d.spaceGroup === spaceGroup
  );

  if (exactMatch) {
    return {
      energyAboveHull: exactMatch.energyAboveHull,
      bandGap: exactMatch.bandGap,
      isMetal: exactMatch.isMetal,
      totalMagnetization: exactMatch.totalMagnetization,
    };
  }

  // If no exact match, find similar materials and average their properties
  const similarMaterials = materialData.filter(
    (d) =>
      d.formula === formula ||
      d.crystalSystem === crystalSystem ||
      d.spaceGroup === spaceGroup
  );

  if (similarMaterials.length === 0) {
    // Return average values if no similar materials found
    const averages = materialData.reduce(
      (acc, curr) => ({
        energyAboveHull: acc.energyAboveHull + curr.energyAboveHull,
        bandGap: acc.bandGap + curr.bandGap,
        isMetal: acc.isMetal || curr.isMetal,
        totalMagnetization: acc.totalMagnetization + curr.totalMagnetization,
        count: acc.count + 1,
      }),
      {
        energyAboveHull: 0,
        bandGap: 0,
        isMetal: false,
        totalMagnetization: 0,
        count: 0,
      }
    );

    return {
      energyAboveHull: averages.energyAboveHull / averages.count,
      bandGap: averages.bandGap / averages.count,
      isMetal: averages.isMetal,
      totalMagnetization: averages.totalMagnetization / averages.count,
    };
  }

  // Calculate weighted average based on similarity
  const predictions = similarMaterials.reduce(
    (acc, curr) => ({
      energyAboveHull: acc.energyAboveHull + curr.energyAboveHull,
      bandGap: acc.bandGap + curr.bandGap,
      isMetal: acc.isMetal || curr.isMetal,
      totalMagnetization: acc.totalMagnetization + curr.totalMagnetization,
      count: acc.count + 1,
    }),
    {
      energyAboveHull: 0,
      bandGap: 0,
      isMetal: false,
      totalMagnetization: 0,
      count: 0,
    }
  );

  return {
    energyAboveHull: predictions.energyAboveHull / predictions.count,
    bandGap: predictions.bandGap / predictions.count,
    isMetal: predictions.isMetal,
    totalMagnetization: predictions.totalMagnetization / predictions.count,
  };
}

export function getOptions(
  selectedFormula?: string,
  selectedCrystalSystem?: string
) {
  const filteredData = materialData.filter((d) => {
    return (
      (!selectedFormula || d.formula === selectedFormula) &&
      (!selectedCrystalSystem || d.crystalSystem === selectedCrystalSystem)
    );
  });

  const formulas = Array.from(
    new Set(filteredData.map((d) => d.formula))
  ).sort();
  const crystalSystems = Array.from(
    new Set(filteredData.map((d) => d.crystalSystem))
  ).sort();
  const spaceGroups = Array.from(
    new Set(filteredData.map((d) => d.spaceGroup))
  ).sort();

  return { formulas, crystalSystems, spaceGroups };
}
